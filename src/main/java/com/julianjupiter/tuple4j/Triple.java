package com.julianjupiter.tuple4j;

public class Triple<T1, T2, T3> {
    private final T1 first;
    private final T2 second;
    private final T3 third;

    public Triple(T1 first, T2 second, T3 third) {
        this.first = first;
        this.second = second;
        this.third = third;
    }

    public T1 getFirst() {
        return first;
    }

    public T2 getSecond() {
        return second;
    }

    public T3 getThird() {
        return third;
    }

    public static <T1, T2, T3> Triple<T1, T2, T3> of(T1 first, T2 second, T3 third) {
        return new Triple<>(first, second, third);
    }

    public <T4> Quadruple<T1, T2, T3, T4> toQuadruple(T4 fourth) {
        return Quadruple.of(this.first, this.second, this.third, fourth);
    }
}
